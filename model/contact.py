class Contact:
    def __init__(self, firstname=None, lastname=None, address=None, home=None,
                 mobile=None, work=None, fax=None,
                 email=None, email2=None, email3=None):
        self.firstname = firstname
        self.lastname = lastname
        self.address = address
        self.home = home
        self.mobile = mobile
        self.work = work
        self.fax = fax
        self.email = email
        self.email2 = email2
        self.email3 = email3



